    private void btnupdateproductupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateproductupdateActionPerformed
             String pId = tfupdateproductsearch.getText();
          String pName = tfupdateproductname.getText();
          String pPrice = tfupdateproductprice.getText();
          String pDescription = tfupdateproductdescription.getText();     
          String pCategory = (String)comboboxupdateproductcategory.getSelectedItem();
          String pStatus = (String)comboboxupdateproductstatus.getSelectedItem();
          try{
              Connection con=connectionProvider.getcon();
              Statement st=con.createStatement();
              st.executeUpdate("update product set pName='"+pName+"',pPrice='"+pPrice+"',pDescription='"+pDescription+"',pCategory='"+pCategory+"',pStatus='"+pStatus+"' where pId='"+pId+"'");
              JOptionPane.showMessageDialog(null,"Successfully updated ");
              setVisible(false);
              new UpdateProduct().setVisible(true);
    }                                         
              catch(Exception e)
          {
            JOptionPane.showMessageDialog(null,e);  
          } 
    }//GEN-LAST:event_btnupdateproductupdateActionPerformed

