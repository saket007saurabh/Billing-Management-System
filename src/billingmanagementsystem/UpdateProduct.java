    private void updateproductbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateproductbtnActionPerformed
         String pId = tfupdateproductsearch.getText();
           try{
              Connection con=connectionProvider.getcon();
              Statement st=con.createStatement();
              ResultSet rs = st.executeQuery("select *from product where pId =  '"+pId+"'");
              if(rs.next())
              {
                  tfupdateproductname.setText(rs.getString(2));
                  tfupdateproductprice.setText(rs.getString(3));
                  tfupdateproductdescription.setText(rs.getString(4));
                  comboboxupdateproductcategory.setSelectedItem(rs.getString(5));
                  comboboxupdateproductstatus.setSelectedItem(rs.getString(6));
                  
                 // tfupdatecontact.setEditable(false);
              }
              else {
                  JOptionPane.showMessageDialog(null,"Product Does Not exist !");
              }      
          }
          catch(Exception e)
          {
            JOptionPane.showMessageDialog(null,e);  
          } 
    }//GEN-LAST:event_updateproductbtnActionPerformed

